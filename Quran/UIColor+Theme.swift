import Foundation


import UIKit

extension UIColor {

    class func appIdentity() -> UIColor {
        return UIColor(rgb: 0xb2dfdb)
    }

    class func secondaryColor() -> UIColor {
        return UIColor(rgb: 0x131313)
    }
}
